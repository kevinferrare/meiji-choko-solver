package org.kevinferrare.meijiChokoSolver.solver.dataStructures;

/**
 * A simple class to store 2D integer coordinates
 * 
 * @author Kévin Ferrare
 * 
 */
public class Coordinate {
	private int x;
	private int y;

	public Coordinate(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
}